const FORTUNE_SIGNS = [...SIGNS_DA_JI, ...SIGNS_JI, ...SIGNS_XIAO_JI];

/**
 * 按概率加权随机抽取今日签
 * 大吉 10% · 吉 60% · 小吉 30%
 * @returns {{ text: string, grade: '大吉'|'吉'|'小吉' }}
 */
function drawFortuneSign() {
  const rand = Math.random() * 100;
  let signs, grade;
  if (rand < 10) {
    signs = SIGNS_DA_JI; grade = '大吉';
  } else if (rand < 70) {
    signs = SIGNS_JI; grade = '吉';
  } else {
    signs = SIGNS_XIAO_JI; grade = '小吉';
  }
  const idx = Math.floor(Math.random() * signs.length);
  return { text: signs[idx], grade };
}

/**
 * 根据日期确定性地返回今日签文（向后兼容）
 * @returns {string}
 */
function getTodaySign() {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000);
  return FORTUNE_SIGNS[dayOfYear % FORTUNE_SIGNS.length];
}

