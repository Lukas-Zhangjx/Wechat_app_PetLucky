/**
 * @returns {string}
 */
function getTodaySign() {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000);
  return FORTUNE_SIGNS[dayOfYear % FORTUNE_SIGNS.length];
}

/**
 * @param {string} petTypeId
 * @returns {object|null}
 */
function getPetType(petTypeId) {
  return PET_TYPES.find((p) => p.id === petTypeId) || PET_TYPES[0];
}

