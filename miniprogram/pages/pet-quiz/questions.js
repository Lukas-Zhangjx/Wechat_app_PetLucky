function shuffle(arr) {
  const a = arr.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function getRandomQuestions(petType, count = 10) {
  let pool;
  if (petType === 'cat')         pool = QUESTIONS_CAT;
  else if (petType === 'rabbit') pool = QUESTIONS_RABBIT;
  else if (petType === 'small')  pool = QUESTIONS_SMALL;
  else                           pool = QUESTIONS_DOG;
  return shuffle(pool).slice(0, Math.min(count, pool.length));
}

